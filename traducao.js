let currentLang = localStorage.getItem("siteLanguage") || "pt";

function getTranslation(key, replacements = {}) {
  let text = translations[currentLang][key] || key;
  for (const [placeholder, value] of Object.entries(replacements)) {
    text = text.replace(`{${placeholder}}`, value);
  }
  return text;
}

function applyTranslations() {
  document.querySelectorAll("[data-translate]").forEach(el => {
    const key = el.getAttribute("data-translate");
    if (translations[currentLang] && translations[currentLang][key]) {
      if (el.tagName === "INPUT" && el.hasAttribute("placeholder")) {
        el.setAttribute("placeholder", translations[currentLang][key]);
      } else if (el.tagName === "TEXTAREA" && el.hasAttribute("placeholder")) {
        el.setAttribute("placeholder", translations[currentLang][key]);
      } else {
        el.innerHTML = translations[currentLang][key];
      }
    }
  });
  
  // Aplica a traduÃ§Ã£o dinÃ¢mica para mensagens de feedback
  const respostaAPI = document.getElementById("respostaAPI");
  if (respostaAPI && !respostaAPI.hasAttribute("data-translate")) {
    respostaAPI.setAttribute("data-translate", "feedback-default");
  }
}

document.addEventListener("DOMContentLoaded", applyTranslations);

function toggleLanguage() {
  currentLang = (currentLang === "pt") ? "en" : "pt";
  localStorage.setItem("siteLanguage", currentLang);
  applyTranslations();
}

function showTranslatedMessage(id, key, replacements = {}) {
  const el = document.getElementById(id);
  if (el) {
    el.textContent = getTranslation(key, replacements);
  }
}
